
public class Student {
	private int rollNo;
	private int regno;
	
	
	
	public Student(int rollNo, int regno) {
		super();
		this.rollNo = rollNo;
		this.regno = regno;
	}
	public int getRollNo() {
		return rollNo;
	}
	public void setRollNo(int rollNo) {
		this.rollNo = rollNo;
	}
	public int getRegno() {
		return regno;
	}
	public void setRegno(int regno) {
		this.regno = regno;
	}

}
