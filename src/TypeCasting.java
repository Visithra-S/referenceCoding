
public class TypeCasting {
	public static void main(String args[]) {
		byte a = 10;
		int b = 2;
		byte c = (byte) b;
		System.out.println(c);

		short d = 5;
		int u = 89;
		short l = (short) u;
		System.out.println(l);

		int t = 9;
		long o = 8797l;
		int x = (int) o;
		System.out.println(x);

		long k = 979887;
		float y = 98798798;
		long z = (long) y;
		System.out.println(z);

		float s = 4375f;
		double q = 45.6645d;
		float m = (float) q;
		System.out.println(m);

		int xtt = 10;
		int yfgfg = 56;
		System.out.println(xtt > yfgfg);

	}
}
